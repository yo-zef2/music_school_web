


def get_boolean(boolean_str):
    if boolean_str == "True":
        boolean_str = True
    else :
        boolean_str = False
    return boolean_str


def get_int(int_str):
    if int_str == "1":
        int_str = 1
    elif int_str == "2":
        int_str = 2
    elif int_str == "3":
        int_str = 3
    elif int_str == "4":
        int_str = 4
    else :
        int_str = 5
    return int_str

